'''
Дан целочисленный список размера N. Если он является перестановкой, то есть
содержит все числа от 1 до N, то вывести 0; в противном случае вывести номер
первого недопустимого элемента.
'''

# Ввод размера списка
N = int(input("Введите размер списка N: "))

# Ввод элементов списка с помощью цикла for
lst = []
print(f"Введите {N} целых чисел:")
for i in range(N):
    num = int(input(f"Элемент {i + 1}: "))
    lst.append(num)

# Создаём вспомогательный список, чтобы отмечать, какие числа от 1 до N встречались
# visited[i] будет True, если число (i+1) уже встречалось
visited = [False] * N  # индексы от 0 до N-1 соответствуют числам от 1 до N

# Проходим по элементам списка
for idx in range(N):
    num = lst[idx]
    
    # Проверяем, является ли число допустимым: должно быть от 1 до N включительно
    if num < 1 or num > N:
        # Недопустимое число — выводим его номер (нумерация с 1!)
        print(idx + 1)
        break
    
    # Проверяем, не встречалось ли это число ранее
    if visited[num - 1]:  # num-1 — индекс, соответствующий числу num
        # Повторное вхождение — значит, это не перестановка
        print(idx + 1)
        break
    
    # Отмечаем, что число num уже встречалось
    visited[num - 1] = True

else:
    # Этот блок выполняется, только если цикл завершился без break
    # То есть все элементы допустимы и уникальны → это перестановка
    print(0)